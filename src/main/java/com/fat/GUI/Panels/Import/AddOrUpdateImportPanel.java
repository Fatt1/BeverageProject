    // Event: Tạo/Cập nhật phiếu nhập
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            // Validate
            if (selectedSupplier == null) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn nhà cung cấp", "Lỗi", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            if (importDetails.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Phiếu nhập phải có ít nhất 1 sản phẩm", "Lỗi", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            // Tạo ImportDTO
            ImportDTO dto = new ImportDTO();
            dto.setImportCode(txtImportID4.getText());
            dto.setSupplierId(selectedSupplier.getId());
            dto.setStaffId(currentStaff.getId());
            dto.setImportDetails(new ArrayList<>(importDetails));
            
            // Tính tổng tiền
            BigDecimal total = importDetails.stream()
                .map(ImportDetailDTO::getSubTotal)
                .reduce(BigDecimal.ZERO, BigDecimal::add);
            dto.setTotalPrice(total);
            dto.setStatus(ImportStatus.PENDING);
            
            if (currentImport == null) {
                // ADD mode
                importService.createImport(dto);
                JOptionPane.showMessageDialog(this, "Tạo phiếu nhập thành công");
            } else {
                // EDIT mode
                dto.setId(currentImport.getId());
                importService.updateImport(dto);
                JOptionPane.showMessageDialog(this, "Cập nhật phiếu nhập thành công");
            }
            
            // Navigate back to ImportPanel
            navigateToImportPanel();
            
        } catch (ValidationException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Lỗi validation", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
    
    // Navigate back to ImportPanel
    private void navigateToImportPanel() {
        Container parent = this.getParent();
        while (parent != null && !(parent.getLayout() instanceof CardLayout)) {
            parent = parent.getParent();
        }
        
        if (parent != null && parent.getLayout() instanceof CardLayout) {
            CardLayout cardLayout = (CardLayout) parent.getLayout();
            
            // Find ImportPanel
            Component[] components = parent.getComponents();
            ImportPanel importPanel = null;
            
            for (Component comp : components) {
                if (comp instanceof ImportPanel) {
                    importPanel = (ImportPanel) comp;
                    break;
                }
            }
            
            // Remove current AddOrUpdateImportPanel
            parent.remove(this);
            
            // Refresh and show ImportPanel
            if (importPanel != null) {
                importPanel.loadData();
            }
            
            // Show ImportPanel card - MainForm uses "IMPORT" as card name
            try {
                cardLayout.show(parent, "IMPORT");
            } catch (Exception e) {
                // Fallback: try other common names
                try {
                    cardLayout.show(parent, "ImportPanel");
                } catch (Exception e2) {
                    cardLayout.first(parent);
                }
            }
            
            // Force repaint
            parent.revalidate();
            parent.repaint();
        }
    }

