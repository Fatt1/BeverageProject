    private void setupTableListeners() {
        // Listener cho jTable1 (sản phẩm): Khi click chọn sản phẩm
        jTable1.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) {
                int row = jTable1.getSelectedRow();
                if (row >= 0) {
                    int productId = (int) jTable1.getValueAt(row, 0);
                    selectedProduct = allProducts.stream()
                        .filter(p -> p.getId().equals(productId))
                        .findFirst()
                        .orElse(null);
                    
                    if (selectedProduct != null) {
                        showProductInfo(selectedProduct);
                    }
                }
            }
        });
        
        // Listener cho jTable2 (chi tiết): Khi click chọn detail
        jTable2.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) {
                int row = jTable2.getSelectedRow();
                if (row >= 0) {
                    int productId = (int) jTable2.getValueAt(row, 0);
                    selectedDetail = receiptDetails.stream()
                        .filter(d -> d.getProductId().equals(productId))
                        .findFirst()
                        .orElse(null);
                    
                    if (selectedDetail != null) {
                        loadDetailToInput(selectedDetail);
                    }
                }
            }
        });
        
        // Double click trên jTable1 để focus vào nhập số lượng
        jTable1.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                if (e.getClickCount() == 2) {
                    int row = jTable1.getSelectedRow();
                    if (row >= 0) {
                        txtQuantity.requestFocus();
                    }
                }
            }
        });
    }
    
    private void loadCategories() {
        DefaultComboBoxModel<CategoryDTO> model = new DefaultComboBoxModel<>();
        model.addElement(new CategoryDTO(0, "Tất cả"));
        
        List<CategoryDTO> categories = categoryService.getAllCategories();
        for (CategoryDTO cat : categories) {
            model.addElement(cat);
        }
        
        cboCategory1.setModel(model);
        cboCategory1.setSelectedIndex(0);
        
        cboCategory1.setRenderer(new DefaultListCellRenderer() {
            @Override
            public Component getListCellRendererComponent(JList<?> list, Object value, 
                                                         int index, boolean isSelected, boolean cellHasFocus) {
                super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);
                if (value instanceof CategoryDTO) {
                    setText(((CategoryDTO) value).getName());
                }
                return this;
            }
        });
    }
    
    private void loadProducts() {
        String keyword = txtSearch1.getText().trim();
        
        List<ProductDTO> products = productService.getAllProducts();
        
        // Filter theo category
        if (selectedCategoryId != null && selectedCategoryId != 0) {
            products = products.stream()
                .filter(p -> p.getCategoryId().equals(selectedCategoryId))
                .toList();
        }
        
        // Filter theo keyword
        if (!keyword.isBlank()) {
            products = products.stream()
                .filter(p -> p.getName().toLowerCase().contains(keyword.toLowerCase()))
                .toList();
        }
        
        // Chỉ hiện sản phẩm còn tồn kho
        products = products.stream()
            .filter(p -> p.getStock() != null && p.getStock() > 0)
            .toList();
        
        allProducts = products;
        fillProductTable(products);
    }
    
    private void fillProductTable(List<ProductDTO> products) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        for (ProductDTO p : products) {
            model.addRow(new Object[]{
                p.getId(),
                p.getName(),
                p.getStock()
            });
        }
    }
    
