    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            if (selectedProduct == null) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm", "Lỗi", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            String qtyStr = txtQuantity.getText().trim();
            
            if (qtyStr.isBlank()) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập số lượng", "Lỗi", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            int quantity = Integer.parseInt(qtyStr);
            
            if (quantity <= 0) {
                JOptionPane.showMessageDialog(this, "Số lượng phải lớn hơn 0", "Lỗi", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            // Kiểm tra tồn kho
            if (selectedProduct.getStock() != null && quantity > selectedProduct.getStock()) {
                JOptionPane.showMessageDialog(this, "Số lượng vượt quá tồn kho (" + selectedProduct.getStock() + ")", "Lỗi", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            // Check trùng sản phẩm
            boolean exists = receiptDetails.stream()
                .anyMatch(d -> d.getProductId().equals(selectedProduct.getId()));
            
            if (exists) {
                JOptionPane.showMessageDialog(this, "Sản phẩm đã có trong danh sách", "Lỗi", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            // Lấy giá bán và giảm giá
            BigDecimal price = selectedProduct.getPrice();
            BigDecimal discountAmount = promotionService.calculateDiscountPrice(selectedProduct.getId(), price);
            // Thành tiền = (Giá bán - Giảm giá) * Số lượng
            BigDecimal subTotal = price.subtract(discountAmount).multiply(new BigDecimal(quantity));
            
            // Tạo detail mới
            ReceiptDetailDTO detail = new ReceiptDetailDTO();
            detail.setProductId(selectedProduct.getId());
            detail.setQuantity(quantity);
            detail.setPrice(price);
            detail.setDiscountAmount(discountAmount);
            detail.setSubTotalAmount(subTotal);
            
            receiptDetails.add(detail);
            fillDetailTable();
            updateTotalMoney();
            clearInput();
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Số lượng phải là số hợp lệ", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (selectedDetail == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm cần xóa", "Lỗi", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Bạn có chắc muốn xóa sản phẩm này?", 
            "Xác nhận", 
            JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            receiptDetails.removeIf(d -> d.getProductId().equals(selectedDetail.getProductId()));
            fillDetailTable();
            updateTotalMoney();
            clearInput();
            selectedDetail = null;
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            if (selectedDetail == null) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm cần sửa", "Lỗi", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            String qtyStr = txtQuantity.getText().trim();
            
            if (qtyStr.isBlank()) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập số lượng", "Lỗi", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            int quantity = Integer.parseInt(qtyStr);
            
            if (quantity <= 0) {
                JOptionPane.showMessageDialog(this, "Số lượng phải lớn hơn 0", "Lỗi", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            // Kiểm tra tồn kho
            ProductDTO product = productService.getProductById(selectedDetail.getProductId());
            if (product != null && product.getStock() != null && quantity > product.getStock()) {
                JOptionPane.showMessageDialog(this, "Số lượng vượt quá tồn kho (" + product.getStock() + ")", "Lỗi", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            // Update detail
            selectedDetail.setQuantity(quantity);
            BigDecimal subTotal = selectedDetail.getPrice().subtract(
                selectedDetail.getDiscountAmount() != null ? selectedDetail.getDiscountAmount() : BigDecimal.ZERO
            ).multiply(new BigDecimal(quantity));
            selectedDetail.setSubTotalAmount(subTotal);
            
            fillDetailTable();
            updateTotalMoney();
            clearInput();
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Số lượng phải là số hợp lệ", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

