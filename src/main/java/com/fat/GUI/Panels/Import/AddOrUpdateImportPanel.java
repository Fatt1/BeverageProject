    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            // Validate input
            if (selectedProduct == null) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm", "Lỗi", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            String priceStr = txtImportPrice.getText().trim();
            String qtyStr = txtQuantity.getText().trim();
            
            if (priceStr.isBlank() || qtyStr.isBlank()) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ giá nhập và số lượng", "Lỗi", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            BigDecimal importPrice = new BigDecimal(priceStr);
            int quantity = Integer.parseInt(qtyStr);
            
            if (importPrice.compareTo(BigDecimal.ZERO) <= 0) {
                JOptionPane.showMessageDialog(this, "Giá nhập phải lớn hơn 0", "Lỗi", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            if (quantity <= 0) {
                JOptionPane.showMessageDialog(this, "Số lượng phải lớn hơn 0", "Lỗi", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            // Check trùng sản phẩm
            boolean exists = importDetails.stream()
                .anyMatch(d -> d.getProductId().equals(selectedProduct.getId()));
            
            if (exists) {
                JOptionPane.showMessageDialog(this, "Sản phẩm đã có trong danh sách", "Lỗi", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            // Tạo detail mới
            ImportDetailDTO detail = new ImportDetailDTO();
            detail.setProductId(selectedProduct.getId());
            detail.setProductName(selectedProduct.getName());
            detail.setImportPrice(importPrice);
            detail.setQuantity(quantity);
            
            // Thêm vào list
            importDetails.add(detail);
            fillDetailTable();
            updateTotalMoney();
            
            // Clear input
            clearInput();
            
            JOptionPane.showMessageDialog(this, "Thêm sản phẩm thành công");
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Giá nhập và số lượng phải là số hợp lệ", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (selectedDetail == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm cần xóa", "Lỗi", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Bạn có chắc muốn xóa sản phẩm này?", 
            "Xác nhận", 
            JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            importDetails.removeIf(d -> d.getProductId().equals(selectedDetail.getProductId()));
            fillDetailTable();
            updateTotalMoney();
            clearInput();
            selectedDetail = null;
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

