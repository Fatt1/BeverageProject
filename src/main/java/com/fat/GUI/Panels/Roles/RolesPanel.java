    private void txtSearchKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String keyword = txtSearch.getText().trim();
            List<RoleDTO> roles = roleService.filterRoleByList(keyword);
            fillTable(roles);
        }
    }//GEN-LAST:event_txtSearchKeyPressed

    private void btnAuthorizationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAuthorizationActionPerformed
        int selectedRow = tblRole.getSelectedRow();
        if(selectedRow == -1 ) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nhóm quyền để phân quyền", "Chưa chọn nhóm quyền", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Object objectId = tblRole.getValueAt(selectedRow, 0);
        int roleId = Integer.parseInt(objectId.toString());
        RoleDTO role = roleService.getRoleById(roleId);
        PermissionRoleDialog permissionDialog = new PermissionRoleDialog((Frame) SwingUtilities.getWindowAncestor(this), true, role);
        permissionDialog.setLocationRelativeTo(this);
        permissionDialog.setVisible(true);
    }//GEN-LAST:event_btnAuthorizationActionPerformed


