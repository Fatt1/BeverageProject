    // Event: Tạo hóa đơn
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            if (receiptDetails.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Hóa đơn phải có ít nhất 1 sản phẩm", "Lỗi", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            // Tạo ReceiptDTO
            ReceiptDTO dto = new ReceiptDTO();
            dto.setCode(txtImportID4.getText());
            dto.setCustomerId(selectedCustomer != null ? selectedCustomer.getId() : null);
            dto.setStaffId(currentStaff.getId());
            dto.setReceiptItems(new ArrayList<>(receiptDetails));
            
            // Tính tổng tiền
            BigDecimal subTotal = receiptDetails.stream()
                .map(d -> d.getPrice().multiply(new BigDecimal(d.getQuantity())))
                .reduce(BigDecimal.ZERO, BigDecimal::add);
            
            BigDecimal totalDiscount = receiptDetails.stream()
                .map(d -> d.getDiscountAmount() != null ? d.getDiscountAmount().multiply(new BigDecimal(d.getQuantity())) : BigDecimal.ZERO)
                .reduce(BigDecimal.ZERO, BigDecimal::add);
            
            BigDecimal totalAmount = receiptDetails.stream()
                .map(ReceiptDetailDTO::getSubTotalAmount)
                .reduce(BigDecimal.ZERO, BigDecimal::add);
            
            dto.setSubTotalAmount(subTotal);
            dto.setTotalDiscountAmount(totalDiscount);
            dto.setTotalAmount(totalAmount);
            
            receiptService.createReceipt(dto);
            JOptionPane.showMessageDialog(this, "Tạo hóa đơn thành công", "Thành công", JOptionPane.INFORMATION_MESSAGE);
            
            // Navigate back to ReceiptPanel
            navigateToReceiptPanel();
            
        } catch (ValidationException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Lỗi validation", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
    
    // Navigate back to ReceiptPanel
    private void navigateToReceiptPanel() {
        Container parent = this.getParent();
        while (parent != null && !(parent.getLayout() instanceof CardLayout)) {
            parent = parent.getParent();
        }
        
        if (parent != null && parent.getLayout() instanceof CardLayout) {
            CardLayout cardLayout = (CardLayout) parent.getLayout();
            
            // Find ReceiptPanel and refresh
            Component[] components = parent.getComponents();
            for (Component comp : components) {
                if (comp instanceof ReceiptPanel) {
                    ((ReceiptPanel) comp).loadData();
                    break;
                }
            }
            
            // Remove current panel
            parent.remove(this);
            
            // Show ReceiptPanel
            try {
                cardLayout.show(parent, "RECEIPT");
            } catch (Exception e) {
                cardLayout.first(parent);
            }
            
            parent.revalidate();
            parent.repaint();
        }
    }

