    private void loadProducts() {
        String keyword = txtSearch1.getText().trim();
        
        // Lấy tất cả sản phẩm
        List<ProductDTO> products = productService.getAllProducts();
        
        // Filter theo category
        if (selectedCategoryId != null && selectedCategoryId != 0) {
            products = products.stream()
                .filter(p -> p.getCategoryId().equals(selectedCategoryId))
                .toList();
        }
        
        // Filter theo keyword
        if (!keyword.isBlank()) {
            products = products.stream()
                .filter(p -> p.getName().toLowerCase().contains(keyword.toLowerCase()))
                .toList();
        }
        
        allProducts = products;
        fillProductTable(products);
    }
    
    private void fillProductTable(List<ProductDTO> products) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        for (ProductDTO p : products) {
            model.addRow(new Object[]{
                p.getId(),
                p.getName(),
                p.getStock()
            });
        }
    }
    
    private void initImportInfo() {
        // Generate mã phiếu nhập tự động: PN + timestamp
        String importCode = "PN" + System.currentTimeMillis();
        txtImportID4.setText(importCode);
        
        // Lấy thông tin staff hiện tại (hardcode staff ID = 1)
        currentStaff = staffService.getStaffById(1);
        if (currentStaff != null) {
            txtImportID5.setText(currentStaff.getFirstName() + " " + currentStaff.getLastName());
        }
        
        // Chưa chọn supplier
        txtImportID6.setText("");
        
        // Tổng tiền = 0
        updateTotalMoney();
    }
    
    private void loadImportData() {
        if (currentImport == null) return;
        
        // Load thông tin header
        txtImportID4.setText(currentImport.getImportCode());
        
        StaffDTO staff = staffService.getStaffById(currentImport.getStaffId());
        if (staff != null) {
            txtImportID5.setText(staff.getFirstName() + " " + staff.getLastName());
            currentStaff = staff;
        }
        
        SupplierDTO supplier = supplierService.getSupplierById(currentImport.getSupplierId());
        if (supplier != null) {
            txtImportID6.setText(supplier.getName());
            selectedSupplier = supplier;
        }
        
        // Load chi tiết
        if (currentImport.getImportDetails() != null) {
            importDetails = new ArrayList<>(currentImport.getImportDetails());
            fillDetailTable();
            updateTotalMoney();
        } else {
            importDetails = new ArrayList<>();
        }
        
        // Disable button Tạo, đổi text thành "Cập nhật"
        jButton2.setText("Cập nhật phiếu nhập");
    }
    
    private void showProductInfo(ProductDTO product) {
        txtProductId.setText(String.valueOf(product.getId()));
        txtProductName.setText(product.getName());
        txtImportPrice.setText("");
        txtQuantity.setText("");
    }
    
    private void loadDetailToInput(ImportDetailDTO detail) {
        ProductDTO product = productService.getProductById(detail.getProductId());
        if (product != null) {
            txtProductId.setText(String.valueOf(product.getId()));
            txtProductName.setText(product.getName());
        }
        txtImportPrice.setText(detail.getImportPrice().toString());
        txtQuantity.setText(String.valueOf(detail.getQuantity()));
    }
    
    private void fillDetailTable() {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        
        for (ImportDetailDTO detail : importDetails) {
            // Lấy tên sản phẩm
            ProductDTO product = productService.getProductById(detail.getProductId());
            String productName = product != null ? product.getName() : "N/A";
            detail.setProductName(productName);
            
            model.addRow(new Object[]{
                detail.getProductId(),
                productName,
                FormatterUtil.toVND(detail.getImportPrice()),
                detail.getQuantity(),
                FormatterUtil.toVND(detail.getSubTotal())
            });
        }
    }
    
    private void updateTotalMoney() {
        BigDecimal total = importDetails.stream()
            .map(ImportDetailDTO::getSubTotal)
            .reduce(BigDecimal.ZERO, BigDecimal::add);
        
        lblTotalMoney.setText(FormatterUtil.toVND(total));
    }
    
    private void clearInput() {
        txtProductId.setText("");
        txtProductName.setText("");
        txtImportPrice.setText("");
        txtQuantity.setText("");
        selectedProduct = null;
        selectedDetail = null;
    }
    
    private void resetForm() {
        // Clear all
        importDetails.clear();
        fillDetailTable();
        
        // Reset supplier
        selectedSupplier = null;
        txtImportID6.setText("");
        
        // Generate new import code
        String importCode = "PN" + System.currentTimeMillis();
        txtImportID4.setText(importCode);
        
        // Clear inputs
        clearInput();
        
        // Update total
        updateTotalMoney();
        
        // Clear selection
        jTable1.clearSelection();
        jTable2.clearSelection();
    }
