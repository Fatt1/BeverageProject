    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblPermission.getModel();
        List<RoleClaimDTO> permissions = new ArrayList<>();
        for (int row = 0; row < model.getRowCount(); row++) {
            String function = (String) model.getValueAt(row, 0);
            int permissionValue = 0;
            Object canReadObj = model.getValueAt(row, 1);
            Object canCreateObj = model.getValueAt(row, 2);
            Object canDeleteObj = model.getValueAt(row, 3);
            Object canUpdateObj = model.getValueAt(row, 4);
            if ((Boolean) canReadObj != null && (Boolean) canReadObj) {
                permissionValue |= Action.READ;
            }
            if ((Boolean) canCreateObj != null && (Boolean) canCreateObj) {
                permissionValue |= Action.CREATE;

            }
            if ((Boolean) canDeleteObj != null && (Boolean) canDeleteObj) {
                permissionValue |= Action.DELETE;
            }
            if ((Boolean) canUpdateObj != null && (Boolean) canUpdateObj) {
                permissionValue |= Action.UPDATE;
            }
            RoleClaimDTO dto = new RoleClaimDTO(
                    null,
                    role.getId(),
                    function,
                    permissionValue
            );
            permissions.add(dto);
        }
        roleService.setRoleClaims(role.getId(), permissions);
        JOptionPane.showMessageDialog(this, "Cập nhật quyền thành công!", "Thành công", JOptionPane.INFORMATION_MESSAGE);
        this.dispose();
    }//GEN-LAST:event_btnSaveActionPerformed

