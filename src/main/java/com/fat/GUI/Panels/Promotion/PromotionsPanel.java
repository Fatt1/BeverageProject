    private void loadData(){
        PagedResult<PromotionViewDTO> result = promotionService.filterPromotionByList(searchKey, selectedStatus, currentPage ,pageSize);
        fillTable(result);
    }

    private void fillTable(PagedResult<PromotionViewDTO> result){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate today = LocalDate.now();
        for (PromotionViewDTO p : result.getItems()){
            String status;
            if (today.isBefore(p.getStartDate())){
                status = "Sắp diễn ra";
            } 
            else if (today.isAfter(p.getEndDate())){
                status = "Kết thúc";
            }
            else{
                status = "Đang diễn ra";
            }
            model.addRow(new Object[]{
                p.getId(),
                p.getName(),
                status,
                p.getStartDate().format(formatter),
                p.getEndDate().format(formatter)
            });
        }
        int totalItems = result.getTotalItems();
        if (totalItems == 0){
            lblPage.setText("0 trên 0");
        } else {
            int from = (currentPage - 1) * pageSize + 1;
            int to = Math.min(currentPage * pageSize, totalItems);
            lblPage.setText(from + " - " + to + " trên " + totalItems);
        }
    }

