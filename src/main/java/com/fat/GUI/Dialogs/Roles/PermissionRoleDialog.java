    private void initTable() {
        Object[] header = new Object[] {
                "TÊN CHỨC NĂNG",
                "XEM",
                "THÊM",
                "XÓA",
                "SỬA",
        };
        // Override isCellEditable để chặn click vào ô null (ô bị ẩn)
        DefaultTableModel model = new DefaultTableModel(header, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                // Cột 0 (Tên) không sửa được
                if (column == 0) return false;
                // Lấy giá trị tại ô đó
                Object value = getValueAt(row, column);
                // Nếu value là null (tức là ẩn) thì không cho edit
                return value != null;
            }

            // Định nghĩa kiểu dữ liệu cho cột để JTable tự chọn Editor mặc định
            @Override
            public Class<?> getColumnClass(int columnIndex) {
                if (columnIndex == 0) return String.class;
                return Boolean.class; // Các cột còn lại là Boolean
            }
        };
        tblPermission.setModel(model);

        Map<String, Integer> permissions = roleService.getRoleClaims(role.getId());

        for(String func : functions) {
            int mask = permissions.getOrDefault(func, 0);
            int supportedMask = Permission.getPermission(func);
            Object canRead = ((supportedMask & Action.READ) != 0) ? ((mask & Action.READ) != 0) : null;
            Object canCreate = ((supportedMask & Action.CREATE) != 0) ? ((mask & Action.CREATE) != 0) : null;
            Object canDelete = ((supportedMask & Action.DELETE) != 0) ? ((mask & Action.DELETE) != 0) : null;
            Object canUpdate = ((supportedMask & Action.UPDATE) != 0) ?  ((mask & Action.UPDATE) != 0) : null;
            model.addRow(new Object[] {
                    func,
                    canRead,
                    canCreate,
                    canDelete,
                    canUpdate,
            });
        }


        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment( JLabel.CENTER );
        tblPermission.getColumnModel().getColumn(0).setCellRenderer(centerRenderer);
        for(int i = 1; i < model.getColumnCount(); i++) {
            tblPermission.getColumnModel().getColumn(i).setCellRenderer(new CheckboxRenderer());
        }
    }

