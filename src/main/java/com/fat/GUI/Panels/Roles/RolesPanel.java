    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tblRole.getSelectedRow();
        if(selectedRow == -1 ) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nhóm quyền để xóa", "Chưa chọn nhóm quyền", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Object objectId = tblRole.getValueAt(selectedRow, 0);
        int roleId = Integer.parseInt(objectId.toString());
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa nhóm quyền này?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
        if(confirm != JOptionPane.YES_OPTION)  {
            return;
        }

        roleService.deleteRole(roleId);
        JOptionPane.showMessageDialog(this, "Xóa nhóm quyền thành công", "Thành công", JOptionPane.INFORMATION_MESSAGE);
        loadData();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int selectedRow = tblRole.getSelectedRow();
        if(selectedRow  == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nhóm quyền để chỉnh sửa", "Chưa chọn nhóm quyền", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Object objectId = tblRole.getValueAt(selectedRow, 0);
        int roleId = Integer.parseInt(objectId.toString());
        RoleDTO selectedRole = roleService.getRoleById(roleId);
        Frame parentFrame = (Frame) SwingUtilities.getWindowAncestor(this);
        AddOrUpdateRole roleDialog = new AddOrUpdateRole(parentFrame, true, selectedRole);
        roleDialog.setLocationRelativeTo(parentFrame);
        roleDialog.setVisible(true);

        loadData();
    }//GEN-LAST:event_btnUpdateActionPerformed

