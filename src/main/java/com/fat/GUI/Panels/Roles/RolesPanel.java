    private void fillTable(List<RoleDTO> roles) {
        DefaultTableModel model = (DefaultTableModel) tblRole.getModel();
        model.setRowCount(0);
        for(var role : roles) {
            Object[] row = new Object[] {
                role.getId(),
                role.getName()
            };
            model.addRow(row);
        }
        DefaultTableCellRenderer centerRender = new DefaultTableCellRenderer();
        centerRender.setHorizontalAlignment(JLabel.CENTER);
       for(int i = 0; i < tblRole.getColumnCount(); i++) {
           tblRole.getColumnModel().getColumn(i).setCellRenderer(centerRender);
       }
    }

