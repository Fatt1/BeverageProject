    private void filterTable(List<SupplierViewDTO> suppliers){
        DefaultTableModel model = (DefaultTableModel)tblSupplier.getModel();
        model.setRowCount(0);
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);
        for(SupplierViewDTO eachSupplier : suppliers ){
            Object[] row = new Object[] {
            eachSupplier.getId(),
            eachSupplier.getName(),
            eachSupplier.getEmail(),
            eachSupplier.getPhoneNumber(),
            eachSupplier.getAddress()
        };
            model.addRow(row);
            
        }
        // Căn giữa tất cả các cột ngoại trừ cột hình ảnh
        for (int i = 0; i < tblSupplier.getColumnCount(); i++) {
            if (i == 1) continue;
            tblSupplier.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
        }
    }
    
